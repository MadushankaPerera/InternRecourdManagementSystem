    private void edit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_btnActionPerformed

        int u = JOptionPane.showConfirmDialog(null, "If you really want to UPDATE?");

        if (u == 0) {

            String sName = suName_txt.getText();
            String sEmail = su_email_txt.getText();
            String stSpec = su_jComboBox1.getSelectedItem().toString();

            try {

                A_DataBase.DBconnect.connect().createStatement().executeUpdate("UPDATE supervisorregistation SET supervisoerName = '" + sName + "', companyName = '" + stSpec + "', supervisorMail_1 = '" + sEmail + "' WHERE supervisorMail_1 = '" + sEmail + "'");
                JOptionPane.showMessageDialog(null, "Update Successfully");

                SearchBar_txt.setText("");
                show_tbl();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Update Unsuccessfully");
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Have a Good Day!");
        }
    }//GEN-LAST:event_edit_btnActionPerformed

    private void SearchBar_txtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchBar_txtKeyReleased

        String Search = SearchBar_txt.getText();

        if (Search.matches("[a-z A-Z][0-9]+")) {
            JOptionPane.showMessageDialog(null, "Enter Valide StudentID Number");
        } else {
            try {
                ResultSet rs = DBconnect.connect().createStatement().executeQuery("SELECT * FROM supervisorregistation WHERE supervisorMail_1 LIKE '%" + Search + "%'");

                jTable2.setModel(DbUtils.resultSetToTableModel(rs));

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
    }//GEN-LAST:event_SearchBar_txtKeyReleased

